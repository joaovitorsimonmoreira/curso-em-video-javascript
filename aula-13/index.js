let c = 1

//condiçoes sao usadas, quando geralmente um codigo necessita ser repetido.

//estrutura de condição, com teste logico emcima

while(c<=7){ //while=(enquanto)
console.log(`passo ${c}`)
c++ // mesma coisa que c=c+1
}

//estrutura de condição, com teste logico embaixo

do{ // do=(faça)
  console.log(`passo ${c}`)
  c++
}while(c<=7)